let Scooter = class {
    constructor(year, color, model) {
        this.year = year;
        this.color = color;
        this.model = model;
    }
}

let Driver = class {
    constructor(name, age, experience) {
        this.name = name;
        this.age = age;
        this.experience = experience;
    }
}

